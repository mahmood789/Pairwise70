#' CD000028_pub4_data - Cochrane Review CD000028.pub4
#'
#' Pairwise meta-analysis data from Cochrane review CD000028.pub4.
#' Contains 163 rows of study-level effect size data.
#'
#' @format A data frame with 163 rows and 27 variables:
#' \describe{
#'   \item{Study}{Study name and year}
#'   \item{Study year}{Publication year (for meta-regression)}
#'   \item{Experimental N}{Sample size in experimental group}
#'   \item{Control N}{Sample size in control group}
#'   \item{Mean}{Effect size estimate}
#'   \item{CI start, CI end}{Confidence interval bounds}
#'   \item{review_doi}{DOI of source Cochrane review}
#'   \item{review_url}{URL of source review}
#'   \item{...}{Additional analysis variables}
#' }
#' @source Cochrane Library \url{https://www.cochranelibrary.com/}
#' @examples
#' data(CD000028_pub4_data)
#' head(CD000028_pub4_data)
#' summary(CD000028_pub4_data)
